    private void luuHoaDon(){
        int magh = maGioHangSelected;
        Date ngaylap = date;
        int tien = tongtien;
        int manvquay = DangNhapBUS.taikhoanlogin.getMaNhanVien();
        if(hdBUS.updateHoaDon(magh, ngaylap, manvquay, tien)){
            inHoaDon();
//            updateGiohang();
        }
    }
    
    private void updateGiohang() {
        GioHangBUS ghBUS = new GioHangBUS();
        if (ghBUS.updateTinhTrangGioHang(maGioHangSelected)) {
            this.dispose();
        }
    }
    
