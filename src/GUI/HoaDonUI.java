    private void hienthiHoaDon() {
        
        ChiTietGioHangBUS ctghBUS = new ChiTietGioHangBUS();
        NhanVienBUS nvBUS = new NhanVienBUS();
        String mahd;
        String maban = "";
        int manvyc = 0;
        int manvtt = 0;
        if (hdBUS.getmaHoaDonTheoMaGH(QuanLyTiemPhoUI.maGioHangSelected) == -1) {
            return;
        } else {
            mahd = hdBUS.getmaHoaDonTheoMaGH(QuanLyTiemPhoUI.maGioHangSelected) + "";
        }

        for (HoaDon hd : hdBUS.getlistHoaDonTheomaGH(maGioHangSelected)) {
//            mahd = hd.getMaHoaDon();
            maban = hd.getMaBan();
            manvyc = hd.getMaNvYeuCau();
            manvtt = hd.getMaNvThanhToan();
        }

        String hoadon = "<html>\n"
                + "<head>\n"
                + "<style>\n"
                + "body {margin-left: 40px;display: flex;justify-content: center;}\n"
                + "th,td {padding: 2px 8px;}\n"
                + "th {border-bottom: 1px solid;}\n"
                + "h5 { margin: 10px 120px;text-align: center;padding: 0 12px;font-size: 20px;border-top: 3px solid;\n"
                + "border-bottom: 1px solid;font-family: sans-serif;font-weight: 600;}\n"
                + "h3 {text-align: center;padding: 0px;}\n"
                + "p {margin-left: 100px;}\n"
                + "ul {list-style: none;}\n"
                + "</style>\n"
                + "</head>\n"
                + "<body>\n"
                + "<div>\n"
                + "<h5>Phở 8</h5>\n"
                + "<h3>HOÁ ĐƠN BÁN HÀNG</h3>\n"
                + "<p>Mã hoá đơn:"
                + mahd
                + "</p>\n"
                + "<table>\n"
                + "    <tr>\n"
                + "        <th>Stt</th>\n"
                + "        <th>Tên món</th>\n"
                + "        <th>Số lượng</th>\n"
                + "        <th>Đơn giá</th>\n"
                + "        <th>Thành tiền</th>\n"
                + "    </tr>\n";

        int stt = 0;
        for (ChiTietGioHang ctgh : ctghBUS.getlistChiTietGH(maGioHangSelected)) {
            hoadon += "    <tr>";
            stt += 1;
            hoadon += "<td>" + stt + "</td>";
            hoadon += "<td>" + ctgh.getTenMon() + "</td>";
            hoadon += "<td>" + ctgh.getSoLuong() + "</td>";
            hoadon += "<td>" + hdBUS.formatSo(ctgh.getDonGia())+ "</td>";
            hoadon += "<td>" + hdBUS.formatSo(ctgh.getThanhTien()) + "</td>";
            tongtien += ctgh.getThanhTien();
            hoadon += "    </tr>";
        }
        String t;
        if(date == null){
            t="";
        }else{
            t = sdf.format(date);
        }
        hoadon += "</table>\n"
                + "<label>Tổng tiền: " +hdBUS.formatSo(tongtien) + " VND</label>\n"
                + "<ul>\n"
                + "<li>Số bàn: " + maban + "</li>\n"
                + "<li>Nhân viên gọi món: " + nvBUS.getTenTheoma(manvyc) + "</li>\n"
                + "<li>Nhân viên thanh toán: " + nvBUS.getTenTheoma(manvtt) + "</li>\n"
                + "<li>Thu ngân:" + DangNhapBUS.taikhoanlogin.getMaNhanVien() + " </li>\n"
                + "<li>Ngày lập: " +t+ "</li>\n"
                + "</ul>\n"
                + "<strong>Cảm ơn quý khách !</strong>\n"
                + "</div>\n"
                + "</body>\n"
                + "</body>\n"
                + "</html>";
        txtHoaDon.setText(hoadon);
    }
