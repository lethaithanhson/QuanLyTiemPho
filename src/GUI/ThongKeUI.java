    private void cboThongKeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboThongKeActionPerformed
        if (cboThongKe.getSelectedIndex() == -1) {
            return;
        }
        switch (cboThongKe.getSelectedIndex()) {
            case 0: {
                pnChonDate.setVisible(false);
                hienthiHoaDonAll();
                break;
            }
            case 1: {
                pnChonDate.setVisible(true);
                pnChonNgay.setVisible(true);
                pnChonThang.setVisible(false);
                pnChonNam.setVisible(false);
                dcsChonNgay.setDate(null);
                break;
            }
            case 2: {
                pnChonDate.setVisible(true);
                pnChonNgay.setVisible(false);
                pnChonThang.setVisible(true);
                pnChonNam.setVisible(false);
                break;
            }
            case 3: {
                pnChonDate.setVisible(true);
                pnChonNgay.setVisible(false);
                pnChonThang.setVisible(false);
                pnChonNam.setVisible(true);
                break;
            }
        }
    }//GEN-LAST:event_cboThongKeActionPerformed

