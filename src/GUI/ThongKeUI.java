    public void hienthiHoaDonAll() {
        hdBUS = new HoaDonBUS();
        
        dtmHoaDon.setRowCount(0);
        soluonghoadon =0;
        tongdoanhthu = 0;
        for (HoaDon hd : hdBUS.getlistHoaDon()) {
            soluonghoadon +=1;
            tongdoanhthu += hd.getTongTien();
            
            Vector<Object> vec = new Vector<>();
            vec.add(hd.getMaHoaDon());
            vec.add(hd.getMaNvQuay());
            vec.add(hdBUS.formatSo(hd.getTongTien()));
            vec.add(hdBUS.formatNgay(hd.getNgayLap()));
            dtmHoaDon.addRow(vec);
        }
        lblSoHoaDon.setText(soluonghoadon+"");
        lblTongDoanhThu.setText(hdBUS.formatSo(tongdoanhthu));
    }
    
