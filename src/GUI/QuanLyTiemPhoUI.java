    public void taoGioHang(boolean n, boolean m) {
        String maban = lblSoBan.getText();
        if (n) {
            MenuGoiMon mnu = new MenuGoiMon(null, true);
            mnu.setVisible(true);
            if (!mnu.isDisplayable()) {
                hienThiGioHang();
            }
        } else {
            if (!ghBUS.taoGioHang(maban)) {
                JOptionPane.showMessageDialog(null, maban + "  Mở bàn thát bại!");
                return;
            } else {
                maGioHangSelected = ghBUS.getMaGioHangbangMaBan(lblSoBan.getText());
                if (m) {
                    JOptionPane.showMessageDialog(null, "Mở bàn thành công !");
                } else {
                    return;
                }
            }
        }
    }

    private void xulyThanhToan() {
        NhanVien nvyc = cboNVyeucau.getItemAt(cboNVyeucau.getSelectedIndex());
        NhanVien nvtt = cboNVthanhtoan.getItemAt(cboNVthanhtoan.getSelectedIndex());
        int manvyc = nvyc.getMaNhanVien();
        int manvtt = nvtt.getMaNhanVien();

        String maban = lblSoBan.getText();
        if (!hdBUS.taoHoaDon(manvyc, manvtt, maban, maGioHangSelected)) {
            return;
        } else {
            HoaDonUI hd = new HoaDonUI(null, true);
            hd.setVisible(true);
            if (!hd.isDisplayable()) {
                if (!ghBUS.checkThanhToan(maGioHangSelected)) {
                    return;
                } else {
                    if (lblLoaiban.getText().equals("M")) {
//                    cập nhật bàn nhỏ
                        boolean check = bnBUS.updateTinhTrangBan();
                        capNhattinhtrangBan(check, mapbtnBanNho);
                    } else if (lblLoaiban.getText().equals("L")) {
//                    Cập nhật bần lớn
                        boolean check = blBUS.updateTinhTrangBan();
                        capNhattinhtrangBan(check, mapbtnBanLon);
                    }
                }
            }
        }
    }

    private void capNhattinhtrangBan(boolean check, HashMap<String, JPanel> map) {
        if (check) {
            JPanel pn = map.get(lblSoBan.getText());
            pn.setEnabled(true);
            xulyBanDaDat(pn);
            cboNVthanhtoan.setSelectedIndex(-1);
            cboNVyeucau.setSelectedIndex(-1);
            xulyEvenBtnThanhToan();
            pnChiTietBan.setVisible(false);
        }
    }

